package selleck.email.pojo;

public class GoogleSearchEmail {
	private int id;
	private int dataId;
	private String emailMark;
	private String fullAuthor;
	private String allEmail;
	private String getEmail;
	private int score;
	private String dateTime;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getDataId() {
		return dataId;
	}
	public void setDataId(int dataId) {
		this.dataId = dataId;
	}
	public String getEmailMark() {
		return emailMark;
	}
	public void setEmailMark(String emailMark) {
		this.emailMark = emailMark;
	}
	public String getFullAuthor() {
		return fullAuthor;
	}
	public void setFullAuthor(String fullAuthor) {
		this.fullAuthor = fullAuthor;
	}
	public String getAllEmail() {
		return allEmail;
	}
	public void setAllEmail(String allEmail) {
		this.allEmail = allEmail;
	}
	public String getGetEmail() {
		return getEmail;
	}
	public void setGetEmail(String getEmail) {
		this.getEmail = getEmail;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	public String getDateTime() {
		return dateTime;
	}
	public void setDateTime(String dateTime) {
		this.dateTime = dateTime;
	}
	
	
	
}
